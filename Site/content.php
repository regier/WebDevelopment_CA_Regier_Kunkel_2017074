        <h2>Trivia</h2>
        <p>
            The set contained just one cube, changed to different colours by means of gel panels.<br>
            Since it was a time-consuming process to change from one to another, the film was not shot in sequence, rather colour-by-colour. The red gels were the first to be installed, meaning all scenes in red rooms were shot first.<br>
            As it happens, red rooms contain the most dialog-heavy scenes in the film, including Worth's big "there is no conspiracy" speech to Holloway.<br>
            The film had a modest budget and a tight schedule, and David Hewlett recalled being very apprehensive at shooting scenes that contained pages of pure dialog on his part very early in the shoot.<br>
            He also felt that Worth's line "Well I feel better" after his rant to Holloway rang immensely true on a personal level, as the remainder of the shoot was much less dependent on his memorisation.
        </p>
